package com.basics;

public class Average {
	public static void main(String args[])
	{
	
		int num1=20;
		int num2=40;
		int quotient;
		int remainder;
		
		
		quotient=num1/num2;
		remainder=num1%num2;
				
		System.out.println("The quotient is :"+quotient);		
		System.out.println("The remainder is :"+remainder);	
	}

}
